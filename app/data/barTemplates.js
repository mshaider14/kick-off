// Helper function to get all templates as a flat array
export function getAllTemplates() {
  const allTemplates = [];
  Object.keys(BAR_TEMPLATES).forEach((category) => {
    BAR_TEMPLATES[category].forEach((template) => {
      allTemplates.push({ ...template, category });
    });
  });
  return allTemplates;
}

// Helper function to search templates
export function searchTemplates(query) {
  if (!query || query.trim() === '') {
    return getAllTemplates();
  }

  const searchTerm = query.toLowerCase();
  return getAllTemplates().filter((template) => {
    return (
      template.name.toLowerCase().includes(searchTerm) ||
      template.description.toLowerCase().includes(searchTerm) ||
      template.keywords?.toLowerCase().includes(searchTerm) ||
      template.message?.toLowerCase().includes(searchTerm)
    );
  });
}

// Helper function to filter templates by category
export function filterTemplatesByCategory(category) {
  if (!category || category === 'all') {
    return getAllTemplates();
  }
  return BAR_TEMPLATES[category]?.map((template) => ({ ...template, category })) || [];
}

// Helper function to get template by ID
export function getTemplateById(id) {
  const allTemplates = getAllTemplates();
  return allTemplates.find((template) => template.id === id);
}
